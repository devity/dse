function dse_font_2_cli($Message,$Return=FALSE){
	global $vars;
	$Message=strtoupper($Message);
	$tbr=array();
	for($i=0;$i<strlen($Message);$i++){
		$c=$Message[$i];
		//print "c=$c ";
		if(!array_key_exists($c, $vars[DSE][FontChars])){
			$c="unknownchar";
		}
		for($L=0;$L<5;$L++){
			$tbr[$L].=$vars[DSE][FontChars][$c][$L]."";
		}		
	}
	if($Return){
		return $tbr;
	}
	for($L=0;$L<5;$L++){
		print $tbr[$L]."\n";
	}
}



